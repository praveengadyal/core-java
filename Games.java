class Games{

private int id;
private String name;
private String mode;
private int noOfPlayers;
private String noOfDownloads;

public int getId(){return id;}

public void setId(int id){this.id=id;}

public String getName(){return name;}

public void setName(String name){this.name=name;}

public String getMode(){return mode;}

public void setMode(String mode){this.mode=mode;}

public int getNoOfPlayers(){return noOfPlayers;}

public void setNoOfPlayers(int noOfPlayers){this.noOfPlayers=noOfPlayers;}

public String getNoOfDownloads(){return noOfDownloads;}

public void setNoOfDownloads(String noOfDownloads){this.noOfDownloads=noOfDownloads;}

}