class MusicPlayer{

private int id;
private String name;
private String totalSongs;
private String totalDownloads;
private double ratings;

public int getId(){return id;}

public void setId(int id){this.id=id;}

public String getName(){return name;}

public void setName(String name){this.name=name;}

public String getTotalSongs(){return totalSongs;}

public void setTotalSongs(String totalSongs){this.totalSongs=totalSongs;}

public String getTotalDownloads(){return totalDownloads;}

public void setTotalDownloads(String totalDownloads){this.totalDownloads=totalDownloads;}

public double getRatings(){return ratings;}

public void setRatings(double ratings){this.ratings=ratings;}


}