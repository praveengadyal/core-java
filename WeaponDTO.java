class WeaponDTO{

private int id;
private String name;
private String producedBy;
private int rateOfFire;
private String type;

public int getId(){return id;}

public void setId(int id){this.id=id;}

public String getName(){return name;}

public void setName(String name){this.name=name;}

public String getProducedBy(){return producedBy;}

public void setProducedBy(String producedBy){this.producedBy=producedBy;}

public int getRateOfFire(){return rateOfFire;}

public void setRateOfFire(int rateOfFire){this.rateOfFire=rateOfFire;}

public String getType(){return type;}

public void setType(String type){this.type=type;}
}